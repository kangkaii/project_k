package kk.practise.高效统计方式;

/**
 * @author kangkai on 2017/10/24.
 */
public class MutableInteger {

    int value;

    public MutableInteger(int val) {
        this.value = val;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
